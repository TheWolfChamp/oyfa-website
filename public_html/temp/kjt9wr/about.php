							<p><h1> The Beginning </h1></p> <p>

								“In 1988 Cyn Romero, Brod Bello, and a few other Filipino students wanted to form an organization that stood out from the other ones that already existed. They wanted one that would be more inclusive, not one that isolated itself from outsiders. This new organization was never meant to be exclusive at all. Its members fully welcomed anyone, Filipino or not, who wanted to hang out with them and who could tolerate – or even contribute to – their quirkiness. To say ‘I can’t join OYFA; I’m not Filipino’ was not a good enough reason not to join; we’d reply, ‘So?’ Moreover, they wanted to be more involved in other parts of the University, not just their own members. Primarily, they wanted the members to have fun, while at the same time learning to get along, work, and have fun. Most important of all, the organization’s members would learn something about their own culture and would share it with others. Why? To truly understand Filipinos – and therefore ourselves – we really need to know where we have come from. So, OYFA was born…”
									—– OYFA alumni Dr. Ron Labuguen
