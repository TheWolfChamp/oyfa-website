		<h1>Really Moving Forward: A Review of Barrio 2011</h1>
		<p><small>- Mary Wang, Asian Student Union</small></p><hr>
<p>
I don't claim to be the most active OYFA member, or the most knowledgeable about culture shows, even though I served as one of three CSA Culture Chairs this past school year, but I do recognize an amazing show when I see one.
</p><p>
The MLK Performance center is a beautiful venue that only served to make Barrio an even greater success. From the tasteful banner, to the perfect execution of stage directions/prop changes, OYFANs used the stage was used perfectly and  filled it with passion.
</p><p>
However, the things that stood out to me were:
</p><p>
1. The demographics of the audience that attended Barrio (many family members, students from surrounding schools & alumni outnumbered UVa students)
</p><p>
2. The overarching theme of Barrio, that was told through live-action skit segments
</p><p>
3. The balance of tradition and current cultural identity that reached across generations
</p><p>
4. The hype and dedication of all those involved with Barrio (97 performers!!!)
</p><p>
The theme of Barrio Fiesta 2011 was Pinaroroonan: Movin Forward, and this was expressed through a skit that was framed around a wedding. During the wedding, the main character, also the best man, (a comical but insightful character) learns about the hardships of Filipino  immigrants through stories told by friends and guests at his best friend's wedding. The skit was touching, educational and comical, all at the same time, if possible. It was evident that the script writers and actors much countless hours into the creation and rehearsal of the skit. The Culture Chairs, Katrina Chua and Keia Del Rosario, indicated that they didn't want to talk about the "Filipino Identity", but inadvertently, the skit did educate the audience about the Filipino identity. It showed,rather than told the audience of what it means to be Filipino and what values are most important to Filipino culture: Family, working hard and Support/loyalty.
</p><p>
The scene was interjected by various dance performances, such as the awe-inspiring, yet humorous Igorot (male coming of age dance) and the beautiful Bulaklakan. Of course, the best was saved for last. Through an exciting 4th Year Tinikling (dance involving hopping between and around bamboo sticks) and the energetic Modern Dance routines, OYFA really showed it's presence, unity, love for OFYA and family values (or should I say OYFamily values?). The audience, or maybe it was just me, experienced a wide range of emotions from beginning to end, from laughing to crying, from awe to excitement. And I believe that even non-Filipinos, such as myself, could have identified with the issues and hardships depicted in the skit.
</p><p>
Overall, I thought that the show was one of the best culture shows I have ever seen. I must applaud the OYFA executive board members, Culture Chairs and of course, all 97 performers who put in hundreds of hours of rehearsal in order to make all of this possible. Did I also mention that the food was DELICIOUS, and all made by the family members of OYFANs or OYFANs themselves??!?!
</p><p>
A job well done, OYFA, I am looking forward to seeing what's in store next year.
	</p>	<hr>
			
